#.format method
name = "Anvi"
age=22

#1st method
print("My name is {} and my age is {}".format(name,age))

#2nd method
print("My name is {n} and my age is {a}".format(n=name,a=age))

salary=10000.3452
print("My salary is {0:.1f}".format(salary))

#fstring method
print(f"My name is {name} and I am {age} years old")